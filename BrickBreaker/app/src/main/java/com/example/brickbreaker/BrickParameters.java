package com.example.brickbreaker;

public class BrickParameters {

    int width;
    int height;
    int positionX;
    int positionY;

    public BrickParameters(int width, int height, int positionX, int positionY) {
        this.width = width;
        this.height = height;
        this.positionX = positionX;
        this.positionY = positionY;
    }
}
